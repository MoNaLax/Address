public class person {
  private String name;
  private String ID;
  private int age;

  public void setName(String name) {
    this.name = name;
  }

  public void setID(String ID) {
    this.ID = ID;
  }

  public String getName() {
    return this.name;
  }

  public String getID() {
    return this.ID;
  }

  public void setAge(int age) {
    this.age = age;
  }

  public int getAge() {
    return this.age;
  }
}
